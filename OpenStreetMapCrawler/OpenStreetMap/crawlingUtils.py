import requests
import ast


def getDistanceDrivingCar(listLocations):
    bodyString= '{"locations":***,"metrics":["distance"]}'
    bodyString=bodyString.replace("***",listLocations)
    body=ast.literal_eval(bodyString)
    headers = {
        'Accept': 'application/json, application/geo+json, application/gpx+xml, img/png; charset=utf-8',
        'Authorization': '5b3ce3597851110001cf6248c6b18993f80b4cd293af8ae16e6fc37c',
        'Content-Type': 'application/json; charset=utf-8'
    }
    call = requests.post('https://api.openrouteservice.org/v2/matrix/driving-car', json=body, headers=headers)
    print(call.status_code, call.reason)
    return call.json()['distances']


